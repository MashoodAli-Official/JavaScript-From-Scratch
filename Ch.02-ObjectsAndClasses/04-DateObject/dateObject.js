"use Strict";

let today = new Date();
console.log(today);
console.log(today.getDate()); //Today Date
console.log(today.getDay()); //Today Day in Number
console.log(today.getFullYear()); //Current Year
console.log(today.getHours()); //Current Hour
console.log(today.getMilliseconds()); //Current Milliseconds
console.log(today.getMinutes()); //Current Minutes
console.log(today.getMonth()); //Current Month
console.log(today.getSeconds()); //Current Seconds
console.log(today.getTime()); //Current Time in Milliseconds