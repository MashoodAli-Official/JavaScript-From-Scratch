let a = 10;
let b = 20;

// Equal ==
console.log(a == b);

// Equal: check both values and type of values.
console.log(a === b);

// Not equal !=
console.log(a != b);

// Greater than >
console.log(a > b);

// Smaller than <
console.log(a < b);

// Greater than or equal >=
console.log(a >= b);

// Greater than or equal <=
console.log(a <= b);

// Not equal: check both values and type of values.
console.log(a !== b);

